    private void txtUpdateEmpNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtUpdateEmpNoActionPerformed
        try
        {
            String empno = txtUpdateEmpNo.getText().trim();
            int empNo = Integer.parseInt(empno);
            emp e = null;
            e = empDAO.findBEmployeeById(empNo);
            if(e!=null)
            {
                txtUpdateEmpNo.setText(e.getEmpNo()+"");
                txtUpdateEmpName.setText(e.getEname());
                txtUpdateEmpAge.setText(e.getEmpAge()+"");
                txtUpdateEmpSal.setText(e.getEmpAge()+"");
                txtUpdateEmpAddress.setText(e.getEname());
                jLabel4.setVisible(true);
                jLabel5.setVisible(true);
                jLabel6.setVisible(true);
                jLabel7.setVisible(true);
                txtUpdateEmpName.setVisible(true);
                txtUpdateEmpSal.setVisible(true);
                txtUpdateEmpAge.setVisible(true);
                txtUpdateEmpAddress.setVisible(true);
                btnUpdate.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Record Not Found !", "Error !",JOptionPane.ERROR_MESSAGE);
                txtUpdateEmpNo.setFocusable(true);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"SQL Error !", "SQL Error !",JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"Pls input digits only !", "Error !",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtUpdateEmpNoActionPerformed

