    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       
      try
      {
          String empNo=txtUpdateEmpNo.getText().trim();
          int eno=Integer.parseInt(empNo);
          emp e = null;
          e = empDAO.findBEmployeeById(eno);
          e.setEname(txtUpdateEmpName.getText());
          e.setEmpSal(Double.parseDouble(txtUpdateEmpSal.getText()));
          e.setEmpAge(Integer.parseInt(txtUpdateEmpAge.getText()));
          e.setEmpAddress(txtUpdateEmpAddress.getText());
          boolean response=empDAO.updateEmployee(e);
          if(response)
          {
              JOptionPane.showMessageDialog(null, "Record updated successfully!","Success!",JOptionPane.INFORMATION_MESSAGE);
              
          }
          else
          {
              JOptionPane.showMessageDialog(null, "Could not update the record!","Error!",JOptionPane.ERROR_MESSAGE);
          }
      }
      catch(NumberFormatException ex)
      {
          JOptionPane.showMessageDialog(null, "Please input digits only!","Error!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
      }
      catch(SQLException ex)
      {
          JOptionPane.showMessageDialog(null, "DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
      }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        txtUpdateEmpNo.requestFocus();
        String empno = txtUpdateEmpNo.getText().trim();
        if(empno.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please input some value !","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            int empNo = Integer.parseInt(empno);
            emp e = empDAO.findBEmployeeById(empNo);
            if(e==null)
            {
                JOptionPane.showMessageDialog(null, "No Record Found !","Not Found",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            txtUpdateEmpName.setText(e.getEname());
            txtUpdateEmpSal.setText(String.valueOf(e.getEmpSal()));
            txtUpdateEmpAddress.setText(e.getEmpAddress());
            txtUpdateEmpAge.setText(String.valueOf(e.getEmpAge()));
            
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter digits only !","Input Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error !", "SQL Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

