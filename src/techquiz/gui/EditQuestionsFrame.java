    // Loading Questions .
    public void loadQuestions()
    {
        try
        {
            ArrayList <Question> questionList = QuestionDAO.getQuestionsByExamId(editExam.getExamId());
            for(Question obj : questionList)
            {
                qstore.addQuestion(obj);
            }
        }
        
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Edit Questions!",JOptionPane.ERROR_MESSAGE);
        }

    }
    
    public void showQuestion()                                  //NullPointerException
    {
        Question question=qstore.getQuestionByQno(qno);
        if(question==null)
        {
            
        }
        txtEditQuestions.setText(question.getQuestion());
        txtEditQuestionsOption1.setText(question.getAnswer1());
        txtEditQuestionsOption2.setText(question.getAnswer2());
        txtEditQuestionsOption3.setText(question.getAnswer3());
        txtEditQuestionsOption4.setText(question.getAnswer4());
        String correctAnswer=question.getCorrectAnswer();
        String correctOption=getKeyFromValue(correctAnswer);
        jcEditQuestionsCorrectOption.setSelectedItem(correctOption);
    }
    
    //Using Hashmap to get key from corresponding values .
    
    public String getKeyFromValue(String value)
    {
        Set<String>keys=options.keySet(); //Creating set to keep all the 4 options as key-value pairs.
        for (String key : keys)           //keySet() method is used to make pairs of key & value .
        {
            String currValue=options.get(key); //currValue is the respective option of ArrayList .
            if (currValue.equals(value)) 
            {
             return key;                  //key is the corresponding value respectively .
            }
        }

        return null;        //will never return null as in any case anyone of the 4 options will be matched with respective keys .
    }                       //but to satisfy java, we returned null .

    
    // validateInput() to validate input is right or not .
    public boolean validateInput()
    {    
        question=txtEditQuestions.getText().trim();
        option1=txtEditQuestionsOption1.getText().trim();
        option2=txtEditQuestionsOption2.getText().trim();
        option3=txtEditQuestionsOption3.getText().trim();
        option4=txtEditQuestionsOption4.getText().trim();
        correctOption=jcEditQuestionsCorrectOption.getSelectedItem().toString();
        if(question.isEmpty()||option1.isEmpty()||option2.isEmpty()||option3.isEmpty()||option4.isEmpty()||correctOption.isEmpty())
            return false;
        else
            return true;
    }   

