    private void btnEditQuestionsNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuestionsNextActionPerformed
        if(!validateInput())
        {
            JOptionPane.showMessageDialog(null, "Please fill all the input fields","Cannot Add Question!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String optionName=options.get(correctOption);
        Question q = new Question(editExam.getExamId(),editExam.getLanguage(),option1,option2,option3,option4,optionName,question,qno);
        qstore.removeQuestion(pos);
        qstore.setQuestionAt(pos,q);
        pos++;
        if(pos>=qstore.getCount())
        {
            JOptionPane.showMessageDialog(null, "Your question set has been successfully updated.\nPress the DONE button to send it to the database ","Exam Added!",JOptionPane.INFORMATION_MESSAGE);
            pos=0;
        }
        showQuestion();
        qno++;
        
        
        if(qno>qstore.getCount())
            qno=1;
        lblEditQuestionsQno.setText("Question no:"+qno);

    }//GEN-LAST:event_btnEditQuestionsNextActionPerformed

    private void btnEditQuestionsPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuestionsPreviousActionPerformed
        if(!validateInput())
        {
            JOptionPane.showMessageDialog(null, "Please fill all the input fields","Cannot Add Question!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String optionName = options.get(correctOption);
        Question q = new Question(editExam.getExamId(), editExam.getLanguage(), option1, option2, option3, option4, optionName, question, qno);
        qstore.removeQuestion(pos);
        qstore.setQuestionAt(pos,q);
        pos--;
        if(pos<0)
        {
            JOptionPane.showMessageDialog(null, "Your question set has been successfully updated.\nPress the DONE button to send it to the database ","Exam Added!",JOptionPane.INFORMATION_MESSAGE);
            pos = qstore.getCount()-1;
        }
        --qno;
        showQuestion();
        
        
        if(qno<=0)
        {
            qno = qstore.getCount();
        }
        lblEditQuestionsQno.setText("Question No: " +qno);
    }//GEN-LAST:event_btnEditQuestionsPreviousActionPerformed

